package cart.ui;

import cart.application.CartItemService;
import cart.domain.Member;
import cart.dto.CartItemQuantityUpdateRequest;
import cart.dto.CartItemRequest;
import cart.dto.CartItemResponse;
import cart.dto.ErrorResponse;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.*;

import java.net.URI;
import java.util.List;

@RestController
@RequestMapping("/cart-items")
public class CartItemApiController {

    private final CartItemService cartItemService;

    public CartItemApiController(CartItemService cartItemService) {
        this.cartItemService = cartItemService;
    }

    @GetMapping
    public ResponseEntity<List<CartItemResponse>> showCartItems(Member member) {
        return ResponseEntity.ok(cartItemService.findByMember(member));
    }

    @GetMapping("/products/{productId}")
    public ResponseEntity<CartItemResponse> showCartItem(@PathVariable Long productId, Member member) {
        CartItemResponse cartItemResponse = cartItemService.findByMemberAndProductId(member, productId);
        return ResponseEntity.ok(cartItemResponse);
    }

    @PostMapping
    public ResponseEntity<Void> addCartItems(Member member, @RequestBody CartItemRequest cartItemRequest) {
        try {
            final CartItemResponse cartItem = cartItemService.findByMemberAndProductId(member, cartItemRequest.getProductId());
            cartItemService.updateQuantity(
                    member
                    , cartItem.getId()
                    , new CartItemQuantityUpdateRequest(cartItem.getQuantity() + 1));
            return ResponseEntity.created(URI.create("/cart-items/" + cartItem.getId())).build();
        } catch (IllegalArgumentException e) {
            Long cartItemId = cartItemService.add(member, cartItemRequest);
            return ResponseEntity.created(URI.create("/cart-items/" + cartItemId)).build();
        }
    }

    @PatchMapping("/{id}")
    public ResponseEntity<Void> updateCartItemQuantity(Member member, @PathVariable Long id, @RequestBody CartItemQuantityUpdateRequest request) {
        cartItemService.updateQuantity(member, id, request);

        return ResponseEntity.ok().build();
    }

    @DeleteMapping("/{id}")
    public ResponseEntity<Void> removeCartItems(Member member, @PathVariable Long id) {
        cartItemService.remove(member, id);

        return ResponseEntity.noContent().build();
    }

    @ExceptionHandler(IllegalArgumentException.class)
    public ResponseEntity<ErrorResponse> handleIllegalArgumentException(IllegalArgumentException e) {
        return ResponseEntity.status(HttpStatus.NOT_FOUND).body(new ErrorResponse(e.getMessage()));
    }
}